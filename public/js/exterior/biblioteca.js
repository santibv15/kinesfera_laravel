const caja1 = document.querySelectorAll(".caja1");
const containerCaja = document.querySelector('.container_cajas1')
caja1.forEach(caja =>{
    caja.addEventListener('click', ()=>{
       containerCaja.classList.toggle('move')
    })
})
const closeModal = document.querySelector('.salir')
closeModal.addEventListener('click', ()=>{
    containerCaja.classList.remove('move')
})


